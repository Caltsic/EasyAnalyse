export type TranslationKey = keyof (typeof messages)['en-US']

export function translate(
  locale: Locale,
  key: TranslationKey,
  params?: Record<string, string | number>,
) {
  let template = String(messages[locale][key] ?? messages['en-US'][key])
  if (!params) {
    return template
  }

  for (const [name, value] of Object.entries(params)) {
    template = template.replaceAll(`{${name}}`, String(value))
  }

  return template
}

export function getStoredLocale(): Locale {
  if (typeof window === 'undefined') {
    return 'zh-CN'
  }

  const stored = window.localStorage.getItem(LOCALE_STORAGE_KEY)
  return stored === 'en-US' ? 'en-US' : 'zh-CN'
}

export function getShapeLabel(locale: Locale, shape: 'rectangle' | 'circle' | 'triangle') {
  switch (shape) {
    case 'rectangle':
      return translate(locale, 'componentRect')
    case 'circle':
      return translate(locale, 'componentCircle')
    case 'triangle':
      return translate(locale, 'componentTriangle')
  }
}
